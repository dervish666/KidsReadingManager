#!/usr/bin/env node

/**
 * Export Demo Snapshot
 *
 * Exports all Learnalot org data from remote D1 into src/data/demoSnapshot.js.
 * Run: node scripts/export-demo-snapshot.js
 */

import { execFileSync } from 'child_process';
import { writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const DEMO_ORG_ID = '3780f673-74e1-4252-91b5-777c9de2760d';

function query(sql) {
  const result = JSON.parse(
    execFileSync(
      'npx',
      ['wrangler', 'd1', 'execute', 'reading-manager-db', '--remote', '--command', sql, '--json'],
      { encoding: 'utf-8', timeout: 30000 }
    )
  );
  return result[0]?.results || [];
}

// Tables to export with their WHERE clauses
const TABLES = [
  { name: 'students', where: `organization_id = '${DEMO_ORG_ID}'` },
  { name: 'classes', where: `organization_id = '${DEMO_ORG_ID}'` },
  {
    name: 'class_assignments',
    where: `class_id IN (SELECT id FROM classes WHERE organization_id = '${DEMO_ORG_ID}')`,
  },
  {
    name: 'reading_sessions',
    where: `student_id IN (SELECT id FROM students WHERE organization_id = '${DEMO_ORG_ID}') AND session_date > date('now', '-90 days')`,
  },
  {
    name: 'student_preferences',
    where: `student_id IN (SELECT id FROM students WHERE organization_id = '${DEMO_ORG_ID}')`,
  },
  { name: 'org_book_selections', where: `organization_id = '${DEMO_ORG_ID}'` },
  { name: 'org_settings', where: `organization_id = '${DEMO_ORG_ID}'` },
  { name: 'term_dates', where: `organization_id = '${DEMO_ORG_ID}'` },
  {
    name: 'users',
    where: `organization_id = '${DEMO_ORG_ID}' AND auth_provider = 'demo'`,
  },
  { name: 'support_tickets', where: `organization_id = '${DEMO_ORG_ID}'` },
  {
    name: 'support_ticket_notes',
    where: `ticket_id IN (SELECT id FROM support_tickets WHERE organization_id = '${DEMO_ORG_ID}')`,
  },
  { name: 'student_reading_stats', where: `organization_id = '${DEMO_ORG_ID}'` },
  { name: 'student_badges', where: `organization_id = '${DEMO_ORG_ID}'` },
];

console.log(`Exporting Learnalot demo data (org: ${DEMO_ORG_ID})...\n`);

const snapshot = {};
let totalRows = 0;

for (const table of TABLES) {
  const snapshotKey = table.key || table.name;
  const sql = `SELECT * FROM ${table.name} WHERE ${table.where}`;
  console.log(`  ${table.name}...`);

  try {
    const rows = query(sql);
    snapshot[snapshotKey] = rows;
    totalRows += rows.length;
    console.log(`    ${rows.length} rows`);
  } catch (error) {
    console.error(`    ERROR: ${error.message}`);
    snapshot[snapshotKey] = [];
  }
}

// Strip API keys from org_settings before committing to snapshot
const SENSITIVE_SETTING_KEYS = ['googleBooksApiKey', 'hardcoverApiKey'];
if (snapshot.org_settings) {
  snapshot.org_settings = snapshot.org_settings.map((row) => {
    if (!row.setting_value) return row;
    try {
      const val = JSON.parse(row.setting_value);
      let changed = false;
      for (const key of SENSITIVE_SETTING_KEYS) {
        if (val[key]) {
          val[key] = '';
          changed = true;
        }
      }
      return changed ? { ...row, setting_value: JSON.stringify(val) } : row;
    } catch {
      return row;
    }
  });
}

// Always include empty arrays for tables with no data
snapshot.user_tour_completions = [];

console.log(`\nTotal: ${totalRows} rows`);

// Write the snapshot file
const output = `/**
 * Demo Environment Data Snapshot — Learnalot School
 *
 * Auto-generated by: node scripts/export-demo-snapshot.js
 * Generated: ${new Date().toISOString()}
 *
 * Contains all org-scoped data for the demo environment.
 * Re-inserted hourly by the demo reset cron.
 */

export const DEMO_ORG_ID = '${DEMO_ORG_ID}';

export const SNAPSHOT = ${JSON.stringify(snapshot, null, 2)};
`;

const outputPath = join(__dirname, '..', 'src', 'data', 'demoSnapshot.js');
writeFileSync(outputPath, output, 'utf-8');

const sizeKB = (Buffer.byteLength(output, 'utf-8') / 1024).toFixed(1);
console.log(`\nWritten to ${outputPath} (${sizeKB} KB)`);

if (parseFloat(sizeKB) > 500) {
  console.warn('\nWARNING: Snapshot exceeds 500KB. Consider trimming reading_sessions.');
}
