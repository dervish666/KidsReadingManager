/**
 * Cross-tenant isolation tests.
 *
 * Static `WHERE organization_id = ?` checks have been audited many times
 * and look solid, but until 2026-05-08 there was no automated regression
 * test that authenticated as Org A and tried to access Org B's resources
 * by ID. This file is that regression net.
 *
 * Strategy: a smart mock D1 that knows which student/session/book belongs
 * to which org. Any `WHERE id = ? AND organization_id = ?` lookup for a
 * mismatched (id, org) pair returns null — the same behaviour real D1
 * would exhibit. We then call each `:id`-bearing route with Org B's
 * resource ID while authenticated as Org A's user and assert 404 (or 403
 * where appropriate). A 200 with foreign data would mean the route
 * silently dropped the org filter.
 *
 * If a route is added that takes an `:id` path param and operates on a
 * tenant-scoped resource, add it to the table here.
 */

